    private void limitarLetras(java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();
        if((c<'a' || c>'z') && (c<'A'|| c>'Z') && (c!='á') && (c!='é')
        && (c!='í') && (c!='ó') && (c!='ú') && (c!='Á') && (c!='É') && (c!='Í') 
        && (c!='Ó') && (c!='Ú') && (c!='ñ') && (c!='Ñ') && (c!=(char)KeyEvent.VK_SPACE)){
            evt.consume(); 
        }
    }

