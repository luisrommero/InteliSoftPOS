    private void tableUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUsuarioMouseClicked
        fila = tableUsuario.rowAtPoint(evt.getPoint());
        Habilitar();
        txtId.setEditable(false);
        if (fila >= 0 ){                          
            txtId.setText(String.valueOf(tableUsuario.getValueAt(fila, 0)));
            txtClave.setText(String.valueOf(tableUsuario.getValueAt(fila, 1)));
            txtNombre.setText(String.valueOf(tableUsuario.getValueAt(fila, 2)));
            txtAp.setText(String.valueOf(tableUsuario.getValueAt(fila, 3)));
            txtAm.setText(String.valueOf(tableUsuario.getValueAt(fila, 4)));
            txtDir.setText(String.valueOf(tableUsuario.getValueAt(fila, 5)));
            txtTipo.setText(String.valueOf(tableUsuario.getValueAt(fila, 6)));
         }
        
    }//GEN-LAST:event_tableUsuarioMouseClicked

